COLORREF rand_dark_colO(){
  COLORREF col;
  int r,g,b;
  do{
    col=rand_col();
    r=col&255;
    g=(col>>8)&255;
    b=(col>>16)&255;
  }while(sqrtf((float)(r*r+g*g+b*b*0.6f))>96.0f || r>48 || g>48 || (r==g && g==b));
  return col;
}

COLORREF rand_dark_col(){
  COLORREF col;
  int r,g,b;
  do{
    r=(rand()*255)/RAND_MAX;
    g=(rand()*255)/RAND_MAX;
    b=(rand()*255)/RAND_MAX;
    col=r|(g<<8)|(b<<16);
  }while(sqrtf((float)(r*r+g*g+b*b*0.8f))>64.0f);
  return col;
}
